print("Bienvenid@s a los operadores aritmetico en Python") 

num1 = 25
num2 = 18

# Operador aritmetico para sumar (+)
suma = num1 + num2
print ("El resultado de la suma es" ,suma)

# Operador aritmetico para resta (-) 
resta = num1 - num2
print ("El resultado de la resta es" ,resta)

# Operador aritmetico para residuo/modulo (%)
residuo = num1 % num2
print ("El residuo de la divison es" ,residuo)

# Operador aritmetico para dividir (/)
division = num1 / num2
print ("El resultado de la division es" ,division)

# Operador aritmetico para division exacta(//)
divisonExacta = num1 // num2
print ("El resultado de la division exacta es" ,divisonExacta)

# Operador aritmetico para potencia(**)
potencia = num1 ** num2
print ("El resultado de la potencia es" ,potencia)

